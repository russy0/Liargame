def fetch_json(url: str) -> dict:
    request = urllib.request.Request(url, headers={"User-Agent": USER_AGENT})
    with urllib.request.urlopen(request, timeout=45) as response:
        return json.load(response)


def wikidata_labels(query: str) -> list[str]:
    url = "https://query.wikidata.org/sparql?" + urllib.parse.urlencode({"query": query, "format": "json"})
    data = fetch_json(url)
    return [binding["label"]["value"] for binding in data.get("results", {}).get("bindings", [])]


def category_members(title: str, *, depth: int = 1, limit: int = 120) -> list[str]:
    seen_categories = {title}
    pending: list[tuple[str, int]] = [(title, 0)]
    words: list[str] = []
    while pending and len(words) < limit:
        category, level = pending.pop(0)
        params = {
            "action": "query",
            "list": "categorymembers",
            "cmtitle": category,
            "cmtype": "page|subcat",
            "cmlimit": "80",
            "format": "json",
        }
        url = "https://ko.wikipedia.org/w/api.php?" + urllib.parse.urlencode(params)
        data = fetch_json(url)
        for member in data.get("query", {}).get("categorymembers", []):
            member_title = member.get("title", "")
            if member.get("ns") == 14:
                if level < depth and member_title not in seen_categories:
                    seen_categories.add(member_title)
                    pending.append((member_title, level + 1))
                continue
            words.append(member_title)
            if len(words) >= limit:
                break
        time.sleep(0.05)
    return words


def clean_word(value: str) -> str | None:
    word = value.strip()
    word = re.sub(r"^분류:", "", word)
    word = re.sub(r"\s*\([^)]*\)", "", word)
    word = re.sub(r"\s+", " ", word).strip()
    if not word:
        return None
    if any(part in word for part in BAD_PARTS):
        return None
    if re.search(r"[:/#\\\[\]{}<>|@]", word):
        return None
    if re.search(r"\d", word):
        return None
    if not re.search(r"[가-힣]", word):
        return None
    compact_length = len(word.replace(" ", ""))
    if compact_length < 1 or compact_length > 14:
        return None
    return word


def merge_words(*word_lists: list[str], limit: int = 120) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for words in word_lists:
        for raw_word in words:
            word = clean_word(raw_word)
            if not word or word in seen:
                continue
            seen.add(word)
            result.append(word)
            if len(result) >= limit:
                return result
    return result


